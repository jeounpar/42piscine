#include "rush01.h"

int	*get_nums(char *str)
{
	int	*arr;
	int	i;
	int	j;

	i = 0;
	j = 0;
	arr = (int *)malloc(200 * sizeof(int));
	while (str[i] != '\0')
	{
		if (str[i] >= '1' && str[i] <= '9')
		{
			arr[j] = str[i] - '0';
			j++;
		}
		i++;
	}
	arr[j] = -1;
	return (arr);
}
